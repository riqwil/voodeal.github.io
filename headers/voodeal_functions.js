function showSelectedCorporate() {
	// Check browser support
	console.log('Storage supported --> ', typeof(Storage) !== "undefined");
	if (typeof(Storage) !== "undefined") {
		var corporate = getCorporate(localStorage.getItem('selected_corporate'))
		if (corporate) {
			document.getElementById("corporate-name").innerHTML = corporate['corporate-name'];
			document.getElementById("corporate-description").innerHTML = corporate['corporate-description'];
			document.getElementById("corporate-location").innerHTML = corporate['corporate-location'];
			document.getElementById("corporate-website").innerHTML = corporate['corporate-website'];
			document.getElementById("corporate-pricerange").innerHTML = corporate['corporate-pricerange'];
			
			document.getElementById("corporate-logo")['src'] = corporate['corporate-logo'];
			document.getElementById("corporate-mainimage")['src'] = corporate['corporate-mainimage'];
			
			// Images 
			if (corporate['corporate-images'] && corporate['corporate-images'].length > 0) {
				var corporate_images_buttons_html = '';
				var corporate_images_slides_html = '';
				for (var i = 0; i < corporate['corporate-images'].length; i++) {
					if (i == 0) {
						corporate_images_buttons_html += '<button type="button" data-bs-target="#carouselExampleCaptions" data-bs-slide-to="'+ i + '" class="active" aria-current="true" aria-label="Slide ' + (i+1) + '"></button>';
						corporate_images_slides_html += '<div class="carousel-item active"><img src="' + corporate['corporate-images'][i] + '" class="img-resp" alt="..." /><div class="carousel-caption d-none d-md-block"></div></div>';
					} else {
						corporate_images_buttons_html += '<button type="button" data-bs-target="#carouselExampleCaptions" data-bs-slide-to="' + i + '" aria-label="Slide ' + (i+1) + '"></button>';
						corporate_images_slides_html += '<div class="carousel-item"><img src="' + corporate['corporate-images'][i] + '" class="img-resp" alt="..." /><div class="carousel-caption d-none d-md-block"></div></div>';
					}
				}
				document.getElementById("corporate-images-buttons").innerHTML = corporate_images_buttons_html;
				document.getElementById("corporate-images-slides").innerHTML = corporate_images_slides_html;
				///console.log('corporate_images_buttons_html --> ', corporate_images_buttons_html);
				///console.log('corporate_images_slides_html --> ', corporate_images_slides_html);
			} 
			
			// Videos
			if (corporate['corporate-videos'] && corporate['corporate-videos'].length > 0) {
				var videos_html = '<h3>Vidéos</h3>';
				for (var i = 0; i < corporate['corporate-videos'].length; i++) {
					//document.getElementById("corporate-video-"+ i)['src'] = corporate['corporate-videos'][i];
					videos_html += '<video width="320" height="240" class="video" controls><source src="' + corporate['corporate-videos'][i] + '" type=video/mp4></video>';
				}
				document.getElementById("corporate-videos").innerHTML = videos_html;
				///console.log('videos_html --> ', videos_html);
			} else {
				//document.getElementById("corporate-videos").innerHTML = 'Aucune vidéo';
			}
		}	
	} else {
		console.log('Browser does not support Web Storage.');
	}
}

